import random
import asyncio
import math
import discord
from discord.ext import commands
intents = discord.Intents.default()
intents.members = True
intents.reactions = True
intents.messages = True
client = commands.Bot(command_prefix = 'c!', intents = intents)
client.remove_command("help")

@client.event
async def on_ready():
	activity = discord.Activity(name="<guild name>", type=3)
	await client.change_presence(status=discord.Status.online, activity=activity)
	print("Bot is ready!")

@client.command()
async def pt(ctx):
    embed = discord.Embed(title = "Periodic Table", color = discord.Colour.green())
    embed.set_image(url = "https://pubchem.ncbi.nlm.nih.gov/periodic-table/Periodic_Table.png")
    await ctx.send(embed = embed)

@client.command()
async def help(ctx):
    helppls = discord.Embed(title = ":white_check_mark: List of Commands", description = "Use the command prefix -> c!", color = discord.Colour.red())
    helppls.add_field(name = "c!pt", value = "Displays Periodic Table",inline = False)
    helppls.add_field(name = "c!element <elementname or elementnumber>", value = "Displays information about an element",inline = False)
    helppls.set_thumbnail(url = client.user.avatar_url)
    await ctx.send(embed = helppls)

